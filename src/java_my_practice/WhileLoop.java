package java_my_practice;

public class WhileLoop {
    public static void main(String[] args) {
        int i = 7;
        while(i <= 10){
            System.out.println("Keep coding");
            i++;
        }
        int apples = 0;
        while (apples <= 8){
            System.out.println("Apples - " + apples);
            apples++;
        }

    }
}
