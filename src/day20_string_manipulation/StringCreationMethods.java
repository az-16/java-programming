package day20_string_manipulation;

/** Below import statement is automatically added by compiler */
import java.lang.*; /** import all classes from java.lang package */

public class StringCreationMethods {
    public static void main(String[] args) {
        String word1 = "potato";
        System.out.println(word1);

        String word2 = new String("tomato");
        System.out.println(word2);

        String word3 = ""; //empty
        System.out.println(word3);

        String word4 = new String();
        System.out.println(word4);

        //empty if u print shows nothing
        System.out.println("java");
        System.out.println(word4);
        System.out.println("Hello Aizat");

    }
}
